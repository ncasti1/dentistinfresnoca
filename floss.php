										<h2>
															
											Extra Floss? Try These Out Tricks!
											<!-- <span>by John Fountain, on 15-April-2015, 3 Comments</span> -->
										</h2>
									</div>
								</div>

								<p><span class="bold">WE KNOW, WE KNOW…</span>We ask you about flossing every time you come visit us! It is an integral part of your oral hygiene routine and essential for healthy gums and teeth. But did you know there’s more to floss than cleaning between your teeth? Dental floss can actually be used for a lot of other things!
								</br>
								</br>
								<span class="bold">Eight Useful Dental Floss Hacks</span>
								</br>	
								Have some extra floss lying around? Don’t have the right equipment and need to get a job done in a pinch? Try these cool dental floss hacks.
								</br>
								</p>
								<p><span class="bold">Lumineers Solve A Variety Of Dental Problems</span></br>
									

								1.<span class="bold">Use floss for perfectly sliced cake.</span> Ever slice a cake and the knife comes up with half of the cake on it? Use flavorless dental floss to make the perfect slice by stretching it taut and gently pressing through the cake. Pull it out when you reach the bottom and voila!</br>
								2.<span class="bold">Don’t have any kitchen twine?</span> Floss will do. Some foods like meat and bacon need to be bound when cooked. Usually kitchen twine takes care of this but what if you run out? Floss (again, flavorless) has got you covered!</br>
								</p>

								<blockquote>
									<p>Did you know there’s more to floss than cleaning between your teeth? Dental floss can actually be used for a lot of other things!</p>
								</blockquote>
								<p>
								3.<span class="bold">Floss can help you start a campfire.</span> Believe it or not, waxed dental floss burns well! Wrap dental floss around a match or a dry piece of wood to increase burn time.</br>
								4.Floss can clean out those hard-to-reach spots in your furniture. You know those small cracks and crevices in your wooden furniture that you can never manage to clean? Well, dental floss is meant to get into nooks and crannies! Try it on your wooden furniture. You’ll thank us later.</br>
								5.<span class="bold">Floss is great for sewing.</span> Whether you’re out of thread or in need of something more durable, floss works well for sewing on buttons and patching up holes on the fly.</br>
								6.Are your freshly-baked cookies stuck to the baking sheet? Floss ‘em! Dental floss can unstick your cookies without leaving anything behind.</br>
								7.<span class="bold">Dental floss can even help in the garden.</span> If you’re training a vine to grow a certain direction, floss can help! Use it to attach the vine loosely to the structure you want the vine to climb.</br>
								8.<span class="bold">Floss makes for an easy DIY clothesline.</span> If you’re camping, in a hotel, or simply don’t have room in the dryer, floss can be used to create your own clothesline. Yes, it’s that strong!</br>
								</br>
								</p>
								<p>
								<span class="bold">Let’s Be Honest, Floss Is Awesome</span>
								</br>
								Whether you’re using floss to patch something up or slice your birthday cake, always remember to use it for what it was made for, to clean in between your teeth! Daily flossing protects your beautiful smile from periodontal disease and tooth loss, so yeah, we’d say it’s pretty awesome!

								Know some more flossing hacks? Post them on our Facebook page! We’d love to hear from you!
								
								
