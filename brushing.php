										<h2>
															
											
											Back To Basics: The DO's And DON'Ts Of Toothbrushing
											<!-- <span>by John Fountain, on 15-April-2015, 3 Comments</span> -->
										</h2>
									</div>
								</div>

								<p><span class="bold">BRUSHING OUR TEETH</span>is something we do everyday. And yet it’s easy to fall into a routine of bad habits if we’re not careful! Developing correct brushing techniques and sticking to them is crucial to the health and appearance of your smile.
								</br>
								</br>	
								</p>
								<p><span class="bold">Toothbrushing DO’s</span></br>
								<br>	
								1.<span class="bold">Use the right technique.</span>Tilt the brush at a 45-degree angle against the gum line and brush using small circles. This method is the most gentle and effective way to remove plaque!</br>
								2.<span class="bold">Brush for the correct amount of time.</span> Proper brushing takes at least two minutes! To make sure you brush for the right amount of time, use a stopwatch or listen to your favorite song while brushing. When deciding how much time to spend brushing your teeth, the important thing to remember is that it’s not about a prescribed amount of time, it’s about cleaning all the surfaces of your teeth.</br>
								3.<span class="bold">Brush morning and night.</span> Brushing your teeth should be an absolutely essential part of your daily and nightly routine. It’s also important to avoid midnight snacking! Sleeping directly after eating significantly raises your risk for cavities.</br>
								4.<span class="bold">Replace your brush.</span> The ADA recommends buying a new toothbrush every 3-4 months. Over time, the bristles of the toothbrush wear down and become less effective at removing plaque and bacteria. It’s also important to swap out your toothbrush after being sick, otherwise the germs from your illness may reinfect you.</br>
								</p>

								<blockquote>
									<p>Proper brushing takes at least two minutes! </p>
								</blockquote>
								<p><span class="bold">Toothbrushing DON’Ts</span>
								<br>
								<br>
								1.<span class="bold">Don’t put yourself at risk for gum recession.</span>Use a soft-bristled toothbrush and refrain from aggressive brushing. Brushing with a hard-bristled toothbrush or using excessive force while brushing can slowly cause gums to recede, exposing the root of the tooth and leading to sensitivity. Not fun!</br>
								2.<span class="bold">Don’t brush immediately after eating.</span> Wait at least 30 minutes after you’ve eaten to brush your teeth. When you eat, bacteria produce acid that temporarily weakens the enamel. Brushing too soon after eating, especially acidic foods, can damage the enamel in its weakened state. After eating, it’s best to rinse your mouth out with water or chew sugarless gum to increase saliva production. These measures will help to wash away bacteria without damaging your teethAre your freshly-baked cookies stuck to the baking sheet? Floss ‘em! Dental floss can unstick your cookies without leaving anything behind.</br>
								3.<span class="bold">Don’t ignore the rest of your mouth. </span> Especially your tongue! Did you know that 90 percent of bad breath is due to a dirty tongue? That’s why it’s so important to thoroughly brush the tongue or use a tongue scraper.</br>
								4.<span class="bold">Don’t underestimate the importance of visiting your dentist. </span> No matter how well you brush, biannual visits to your dentist are imperative to keeping your smile radiant and healthy! Professional cleanings and checkups are the easiest way to avoid dental problems in the future.</br>
								</br>
								</p>
								<p>
								<span class="bold">Little Things Can Make A Big Difference!</span>
								</br>
								What you do everyday has an effect on the health and beauty of your smile. We hope these little tips will make a big difference in your oral hygiene routine!
								
								
