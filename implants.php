								Though dental care has improved immensely, many Americans still suffer from

								tooth loss. Tooth decay, gum disease, or injury lead to missing teeth and gaps

								between teeth. Dentures used to be the only option for missing teeth but with a

								success rate around 98 percent, dental implants are a great option for those with

								missing teeth who are healthy enough for oral surgery.<br>
								Used to replaced one tooth

								or a set of missing teeth, many patients prefer dental implants to other tooth loss

								solutions because they look and feel like natural teeth. The greatest benefit to

								implants, however, is that they do not require invasive alterations to surrounding

								teeth. Because they can stand alone, neighboring teeth are left healthy and

								untouched.<br>

								<span class="bold">Benefits of Dental Implants</span><br>
							
								Dental implants offer improved function and look when compared to bridges and

								dentures, including:<br>

								- Appearance – dental implants look, feel, and fit like natural teeth, so no one

								will know they aren’t real.<br>

								- Speech and Eating – because dental implants are permanent, they do not slip

								while speaking or eating. Patients also like that they are permanent so they

								don’t have to remove them to clean them or eat.<br>

								- Durability – with proper care, dental implants can last forever, compared to

								five to seven years for dental bridges.<br>

								- Oral health – other prosthetic solutions require reducing the other teeth.

								Implants leave surrounding teeth intact and fully functional. And as an added

								bonus – implants can’t get cavities!<br>

								- Comfort – patients report dental implants are more comfortable than

								removable dentures. They are also more predictable due to their reliable,

								long-term results and 98 percent patient satisfaction.<br>

								- A beautiful face – dental implants help support and maintain the natural look

								of your face. Missing teeth can cause your face to sag and appear sunken and

								older.<br>

								<span class="bold">Dental Implant Process</span><br>

								Dr. Castillo will develop an individualized treatment plan to address your specific

								needs. A tiny post made of titanium will be placed in the gum and over the next

								several months, the implant will integrate with the jawbone. Once the implant has

								bonded to the gums, another post will be placed to securely hold the tooth. A new

								tooth or teeth will be made of an impression of your teeth.<br>
								The replacement tooth or

								crown will then be attached to the post. The new teeth will be made to match the

								color of your natural teeth and will look, feel, and act just as a normal tooth. The

								result is a natural and beautiful looking smile.<br>

