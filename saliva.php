											Saliva: The Unsung Hero of Oral Health
											<!-- <span>by John Fountain, on 15-April-2015, 3 Comments</span> -->
										</h2>
									</div>
								</div>

								<p><span class="bold">EVER THINK ABOUT HOW GREAT</span> your saliva is? Probably not. We’d love to enlighten you! Saliva has an all-important role in your oral and digestive health
								</br>
								</br>
								<span class="bold">Saliva Has Many Important Functions</span>
								</br>	
								Our bodies make two to four pints of saliva a day. That means that <span class="bold">over a lifetime, a person will create enough saliva to fill two swimming pools!</span> So, why is saliva important? Well, there’s more than one answer to that. Besides allowing us to give wet willies or make spit wads when we were kids, our saliva has many important functions.
								<br>
								<br>
								First, saliva aids in digestion. It begins the process of breaking down food and helps us chew, taste and swallow. In fact, without our spit, we wouldn’t be able to taste at all!
								<br>
								<br>
								Additionally, <span class="bold">saliva is essential to maintaining our oral health.</span> Our spit contains antimicrobial agents that protect teeth and defend against bacteria. It also contains minerals such as calcium and phosphate that remineralize our teeth, strengthening the enamel.
								<br>
								<br>
								<span class="bold">Your saliva plays an especially important role after eating and drinking.</span> It washes away that extra food and debris left in your mouth that contributes to decay. It also helps neutralize the acids created by bacteria that break down enamel and cause cavities. Thank you, saliva!

								</p>

								<blockquote>
									<p>Our bodies make two to four pints of saliva a day.</p>
								</blockquote>
								<p>
								<p><span class="bold">Some People Do Not Create Enough Saliva</span></br>
									

								Some people have a condition called <span class="bold">dry mouth</span>, where they aren’t producing enough saliva. Certain illnesses and medications can cause dry mouth, and those who have it are more prone to tooth decay and gum disease as a result. For those with and without dry mouth, <span class="bold">here are some tips to increase saliva production and protect your teeth:</span>
								<br>
								<br>
									-	Chew sugar-free gum, especially after meals<br>
									-	Suck on sugarless candy<br>
									-	Drink plenty of water<br>	
								<br>
								<span class="bold">Saliva Works Around the Clock to Protect Our Smiles.</span> Saliva may just be the unsung hero of our oral health. <span class="bold">It is constantly strengthening and defending our teeth against bacteria, decay and dental disease.</span> At the end of the day, all we can say is that our bodies are amazing and our spit is awesome!
								</br>
								</p>
