										<h2>
															
											Your Child’s Baby Teeth Are Important
											<!-- <span>by John Fountain, on 15-April-2015, 3 Comments</span> -->
										</h2>
									</div>
								</div>

								<p>"<span class="bold">DO I NEED TO FIX</span>my child’s baby tooth since it’s going to fall out anyway?” This is a question we get asked a lot as dental professionals. The answer is a resounding <span class="bold">YES!</span> Primary, or “baby,” teeth serve an important purpose in your child’s present and future oral health.
								</br>
								</br>
								<span class="bold">Baby Teeth Have Three Main Functions</span>
								</br>	
								Besides providing an aesthetic appeal to your child’s smile and boosting their self-esteem, primary teeth have three main functions:
								</br>
								</p>
								<p>
								1.They <span class="bold">aid in proper chewing</span>,fostering good nutrition</br>
								2.They <span class="bold">promote proper speech development</span></br>
								3.They <span class="bold">reserve a space</span>for permanent teeth to grow in<br>
								<br>
								If a primary tooth falls out or must be removed before its time due to decay, the surrounding teeth may shift into the gap, causing dental crowding and future orthodontic problems. In some cases,<span class="bold>"> untreated infections in baby teeth can even affect the developing permanent teeth.</span>
								<br>
								<br>
								Taking care of those primary teeth not only prevents decay, but also helps your child get into the habit of good oral hygiene and sets the stage for their future oral health. <span class="bold">Baby teeth may fall out but they’re still very important!</span>

								</p>


								<blockquote>
									<p>If a primary tooth falls out or must be removed before its time due to decay, the surrounding teeth may shift into the gap, causing dental crowding and future orthodontic problems.</p>
								</blockquote>
								<p>
								<span class="bold">Regular Dental Visits Make For A Lifetime of Healthy Smiles</span>
								<br>
								Your child’s first dental visit is an important milestone! Children should visit the dentist when their first tooth appears or around their first birthday.
								<br>
								<br>
								A February 2005 Children’s Dental Health Project <a href="http://www.wda.org/wp-content/uploads/2012/04/DentalHome-EMarketing_Oct10.html">report</a> found that children who visited the dentist by age one were less likely to visit the emergency room for oral health problems. They also found that children age five who had their first dental visit by the age of one incurred less oral health-related costs than their peers who didn’t see a dentist until age five.
								<br>
								<br>
								On your child’s first visit, <span class="bold">we will check their mouth for decay and other problems as well as show parents proper cleaning techniques</span>. Another benefit of early, regular dental visits is that we can determine if a child is at a higher risk for cavities and implement appropriate intervention.
								<br>
								<br>
								<span class="bold">Baby Teeth Matter</span>
								<br>
								Even though they’re temporary, your child’s baby teeth are essential to their growth and development. Teach children to take care of their baby teeth and <span class="bold">set them on a lifelong path of good oral hygiene</span>. If you have any questions, call us or comment below!
								</br>
								</p>
								<p>
								<span class="bold">We love our patients and their beautiful smiles!</span>
